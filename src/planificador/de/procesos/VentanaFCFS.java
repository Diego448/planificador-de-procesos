    private void botonAñadirProcesoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_botonAñadirProcesoMouseClicked
        //Adición de procesos al arreglo de procesos.
        procesos[apuntadorSiguienteProceso].nombre = campoNombreProceso.getText();
        procesos[apuntadorSiguienteProceso].duracion = Integer.parseInt(campoDuracionProceso.getText());
        if(apuntadorSiguienteProceso < 4) {
            apuntadorSiguienteProceso++;
        } else {
            botonAñadirProceso.setEnabled(false);
        }
        actualizar();
    }//GEN-LAST:event_botonAñadirProcesoMouseClicked

