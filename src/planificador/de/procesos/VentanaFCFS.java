    ActionListener ejecutadorDeTarea = new ActionListener() {
      public void actionPerformed(ActionEvent evt) {
        //Cambiar los valores del campo de texto del procesador.
        campoProcesadorNombre.setText(procesos[0].nombre);
        campoProcesadorDuracion.setText(String.valueOf(procesos[0].duracion));
        //Disminuye el valor de la varaible duración hasta -1.
        if(procesos[0].duracion > -1) {
            procesos[0].duracion--;
        }
        //Llamada al método cambio de proceso y al método actualizar si la duración del proceso actual es menor a 0.
        if(procesos[0].duracion == -1) {
            cambioDeProceso();
            actualizar();
        }
        //Detiene la ejecución de la variable de tipo Timer si el valor del campo nombre del proceso es una cadena vacía.
        if("".equals(procesos[0].nombre)){
            campoProcesadorNombre.setText("null");
            campoProcesadorDuracion.setText("null");
            intervalo.stop();
            botonAñadirProceso.setEnabled(true);
        }
      }
    };
    
    
