    //Actualiza el arreglo gráfico de la ventana.
    public void actualizar() {
        campoNombreProceso1.setText(procesos[0].nombre);
        campoDuracionProceso1.setText(String.valueOf(procesos[0].duracion));
        campoNombreProceso2.setText(procesos[1].nombre);
        campoDuracionProceso2.setText(String.valueOf(procesos[1].duracion));
        campoNombreProceso3.setText(procesos[2].nombre);
        campoDuracionProceso3.setText(String.valueOf(procesos[2].duracion));
        campoNombreProceso4.setText(procesos[3].nombre);
        campoDuracionProceso4.setText(String.valueOf(procesos[3].duracion));
        campoNombreProceso5.setText(procesos[4].nombre);
        campoDuracionProceso5.setText(String.valueOf(procesos[4].duracion));
    }
    
    //Ejecuta las acciones necesarias para realizar el proceso de FCFS.
    public void cambioDeProceso() {
        for(int i = 0; i < apuntadorSiguienteProceso; i++) {
            procesos[i].nombre = procesos[i + 1].nombre;
            procesos[i].duracion = procesos[i + 1].duracion;
        }
        procesos[apuntadorSiguienteProceso].nombre = "";
        procesos[apuntadorSiguienteProceso].duracion = 0;
        if(apuntadorSiguienteProceso > 0) {
            apuntadorSiguienteProceso--;
        }
    }
    
